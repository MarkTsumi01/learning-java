public class exampleOperator {
    public static void main(String[] args) {
        /*
        Operator in Java
        =
        -
        *
        /
        %
        ++
        --
         */

        byte numBer1 = 10;
        byte numBer2 = 20;

        System.out.println(numBer1 + numBer2);
        System.out.println(numBer1 - numBer2);
        System.out.println(numBer1 * numBer2);
        System.out.println(numBer1 / numBer2);
        System.out.println(numBer1 % numBer2);
        System.out.println(numBer1 == numBer2);
        System.out.println(++numBer1);
        System.out.println(--numBer2);

    }

}
